package com.favas.core.JavaOffline;

public class Task9 {
	public static void main(String[] args) 
	{
		
		
        for(int i=1;i<6;i++)
         {
        	 if(i%2!=0) 
             	 {
        	         for(int j=1;j<6;j++)
                     	 {
        		    	     System.out.print(j);
        	     
                     	 }
                     	 }
        	  else
        	     {
        	         for(int k=5;k>=1;k--)
        	        	 {
        	        		 System.out.print(k);
        	        	 }
        	     }
        	System.out.println();
         }
	}	 
      
         
	}  

